<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
           <div v-if="salesman">扫一扫，加微信</div>
           <div v-else>微信扫一扫</div>
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <el-dropdown-item divided @click.native="logout">
            <img v-if="salesman" style="width: 100px;height: 100px;" src="https://debfc14.webp.li/2025/01/ec66217d0b5b74182ed8737839b05012.jpg" class="user-avatar">
            <img v-else style="width: 100px;height: 100px;" src="https://debfc14.webp.li/2025/01/126ca639dd7951802447cbed53e03942.jpg" class="user-avatar">
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'

export default {
  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  data () {
    return {
      salesman: ''
    }
  },
  created () {
    const urlParams = new URLSearchParams(window.location.search)
    this.salesman = urlParams.get('name')
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

